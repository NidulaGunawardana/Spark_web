                    <div class="col-12 col-lg-10 offset-lg-1">
                        <h4 class="text-white">I. First Phase: Weather Balloon</h4>

                        <p class="lead mb-3 text-secondary my-auto text-justify">Before proceeding to the main stage,
                            a high-altitude weather balloon is planned to be launched into the atmosphere to obtain
                            comprehensive knowledge of the surroundings. The primary goal of this phase is to determine
                            the concentration of contaminants at different atmospheric levels that harm the environment
                            and contribute to greenhouse gases. The plan is to achieve these targets by employing air
                            quality sensors and relevant electronic circuits. Considering the cost-inefficiency of
                            other popular approaches of monitoring air pollution, it has been decided to employ this
                            effective method to gather necessary information about our atmosphere.

                        </p>

                    </div>
                </div>
                <div class="row">

                    <div class="col-12 col-lg-10 offset-lg-1">
                        <h4 class="text-white">II. Second Phase: Cube Satellite</h4>
                        <p class="lead mb-3 text-secondary my-auto text-justify">The second phase of the Phoenix Project
                            involves the installation of the Cube Satellite, which is a kind of research spacecraft that
                            has standard dimensions and is lightweight. The objectives of launching a Cube Satellite into
                            low Earth orbit include observing the Earth, testing new communications technology and performing
                            miniature experiments. To successfully accomplish the expected goals, a team of outstanding
                            undergraduates has been selected. The main team is divided into five sub-teams, each with an
                            exceptionally talented leader to guide the team towards success. Following are the five sub-teams,
                            along with their assigned tasks.
                        </p>

                        <ol class="list-group list-group-numbered">
                            <li class="list-group-item text-secondary lead">Power Subsystem:</li>
                            <p class="lead mb-3 text-secondary my-auto text-justify">The power subsystem generates,
                                stores and distributes power to the other subsystems of the satellite by integrating
                                power management and distribution electronics with power sources.</p>
                            <li class="list-group-item text-secondary lead">Communication Subsystem:</li>
                            <p class="lead mb-3 text-secondary my-auto text-justify">This subsystem enables the satellite
                                to transmit and receive data to and from the ground. It may include antennas, transceivers, and modems.</p>
                            <li class="list-group-item text-secondary lead">Altitude Determination and Control Subsystem:</li>
                            <p class="lead mb-3 text-secondary my-auto text-justify">This subsystem determines and controls the
                                orientation of the satellite in space, using sensors like gyroscopes and magnetometers, as well as thrusters or other actuators.</p>
                            <li class="list-group-item text-secondary lead">Structural, Payload, and Thermal Subsystem:</li>
                            <p class="lead mb-3 text-secondary my-auto text-justify">This subsystem is responsible for providing physical support and enclosure for
                                the other subsystems, as well as maintaining the temperature of the satellite within acceptable limits. Additionally, mounting cameras
                                to take pictures and installing a spectrometer to analyze the composition of the atmosphere come under this subsystem.</p>
                            <li class="list-group-item text-secondary lead">Onboard Computing Subsystem:</li>
                            <p class="lead mb-3 text-secondary my-auto text-justify">This is responsible for executing the software and algorithms that control the operations of the satellite, as well as for processing and storing data.</p>

                        </ol>

                    </div>
