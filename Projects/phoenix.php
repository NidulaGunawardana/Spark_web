                        <p class="lead mb-3 text-secondary my-auto text-justify">In this section, we will primarily focus on the initial stage of the project,
                            the weather balloon and shed light on its communication subsystem.</p>
                        <p class="lead mb-3 text-secondary my-auto text-justify">Central to the success of the weather balloon project is the communication
                            subsystem. This critical component ensures seamless data transmission from the high-altitude balloon to the ground station as well
                            as tracking the location of the balloon. Real-time access to the collected data is essential for monitoring and analysis, making the
                            communication subsystem a pivotal aspect of the entire venture. </p>
                        <h6 class="text-white">LoRa Radio Transceiver 433MHz:</h6>
                        <div class="row">
                            <div class="col-lg-6 col-12 text-justify">
                                <div class="row h-100">
                                    <div class="col-12 my-auto">
                                        <img src="../images/LoRa.png" alt="coral pic" class="img-fluid w-100">
                                    </div>
                                </div>
                            </div>

                            <div class="col-lg-6 col-12 text-justify my-auto">
                                <p class="lead mb-3 text-secondary my-auto text-justify">As the primary communication device, the LoRa Radio Transceiver operating at
                                    433MHz plays a pivotal role in sending data over long distances. Unlike traditional communication methods such as WiFi or Bluetooth,
                                    which operate in the crowded 2.4GHz spectrum, LoRa offers an alternative in the license-free ISM frequency bands, providing an extended
                                    communication range. The magic of LoRa lies in its simplicity and efficiency. These radio modules eliminate the need for complex association,
                                    pairing, or scanning processes. </p>
                            </div>
                        </div>

                        <p class="lead mb-3 text-secondary my-auto text-justify">Instead, users can transmit data effortlessly, and any module tuned to the same frequency
                            with the matching encryption key will receive the information. This feature makes LoRa modules ideal for scenarios where long-distance communication is essential</p>
                        <p class="lead mb-3 text-secondary my-auto text-justify">One notable advantage of LoRa packet radios is their simplicity compared to WiFi
                            or Bluetooth Low Energy (BLE). There's no need to worry about maintaining connections, as these modules handle packetization,
                            error correction, and auto-retransmission. This results in a more straightforward and power-efficient communication process.</p>
                        <h6 class="text-white">SPOT Trace Satellite Tracking Device:</h6>
                        <div class="row">


                            <div class="col-lg-6 col-12 text-justify my-auto">
                                <p class="lead mb-3 text-secondary my-auto text-justify">In addition to the LoRa Radio Transceiver, a weather balloon project may incorporate a secondary
                                    communication device, such as the SPOT Trace Satellite Tracking device. Satellite trackers operate by relying on a network of satellites in orbit to receive
                                    position signals, offering global coverage as long as the antenna is pointed towards the sky.</p>
                            </div>
                            <div class="col-lg-6 col-12 text-justify">
                                <div class="row h-100">
                                    <div class="col-12 my-auto">
