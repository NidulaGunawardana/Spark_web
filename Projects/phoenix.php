                    <div class="col-12 col-lg-10 offset-lg-1">
                        <h4 class="text-white">Power Sub System</h4>
                        <p class="lead mb-3 text-secondary my-auto text-justify">Power sub system plays a crucial role in Phoenix project which
                            is responsible for generating, storing and distributing power to the other four subsystems by integrating power management
                            and distribution electronics along with power sources. Following is an overview of the significant aspects of this subsystem.</p>
                        <p class="lead mb-3 text-secondary my-auto text-justify">Given the high-altitude operation of the weather balloon and cube
                            satellite, numerous challenges must be overcome in this subsystem. The system should be able to operate in low temperatures
                            (as low as -75°C). Due to the lightweight nature of the mission, batteries with high energy density are required. Another
                            critical factor that should be focused on is battery duration. Several power-saving modes should be applied to supply power
                            throughout the entire mission duration. </p>
                        <p class="lead mb-3 text-secondary my-auto text-justify">There are different possible configurations that can be employed
                            to meet the expected requirements, including a single battery pack, a dual battery pack and an in-flight battery charging
                            system with solar panels.</p>
                        <p class="lead mb-3 text-secondary my-auto text-justify">It has been decided to mainly use two power sources to supply
                            power to each component. One battery pack will be utilized for devices with higher power requirements that operate
                            for a shorter time, such as Raspberry Pi, cameras, etc. Another battery pack will be used for devices that require
                            operation for a longer duration, like communication or tracking devices. Additionally, another battery pack will be
                            used as a backup power source in case of a power failure. Both of these implementations would require an active heating
                            mechanism because battery performance decreases drastically at temperatures below -20°C/-40°C.</p>
                        <p class="lead mb-3 text-secondary my-auto text-justify">For the battery charging method, series-connected solar panels
                            mounted on the payload box can be employed to charge batteries during the flight. However, the difficulty is that solar
                            panels require additional circuitry, including systems for battery charging and particularly MPPT regulators.</p>

                        <p class="lead mb-3 text-secondary my-auto text-justify">Battery selection is another factor that should be paid attention
                            to carefully. It is a well-known fact that the atmospheric temperature tends to decrease at higher altitudes. As battery
                            performance typically declines in low temperatures, a similar impact can be expected at high altitudes. There are many
                            possible batteries widely used in many applications, such as Li-SO2 , Ni-Cd and Lithium-ion. Let’s discuss the advantages
                            and drawbacks of each possible battery.</p>
                        <ul class="list-group list-group-dots">
                            <li class="list-group-item text-secondary lead">Lithium Sulfur Dioxide (Li-SO2) batteries have been found to satisfy
                                all of the typical requirements. The nominal voltage is about 2.9V and they are available in a wider capacity range.
                                They also have better performance at low temperatures.</li>
                            <li class="list-group-item text-secondary lead">Nickel Cadmium (Ni-Cd) has a high energy density. The most significant
                                drawback of this is the memory effect. The thermal properties are the same as those of Li-ion batteries. </li>
                            <li class="list-group-item text-secondary lead">Nickel Cadmium (Ni-Cd) has a high energy density. The most significant
                                drawback of this is the memory effect. The thermal properties are the same as those of Li-ion batteries. </li>
                        </ul>
                        <p class="lead mb-3 text-secondary my-auto text-justify">In the Pheonix project, there are some other issues that arise.
                            Because the voltage and current requirements of the various components differ, multiple voltage regulators and buck/boost
                            converters are required. To safeguard batteries from very low temperatures, passive insulation or the active heating method
                            would be appropriate. However, implementing active heating would add additional cost and increase power consumption. Another
                            challenge arises with balancing power across parallel battery strings.</p>
                        <p class="lead mb-3 text-secondary my-auto text-justify">As a critical but challenging subsystem of the Phoenix project,
                            the power subsystem is responsible for a variety of functions. However, with the collective support and dedication of
                            the exceptionally talented undergraduates working in this subsystem, the challenges will not be difficult to overcome.</p>

