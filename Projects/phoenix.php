                            </div>
                        </div>
                        <p class="lead mb-3 text-secondary my-auto text-justify">The SPOT Trace is particularly effective for tracking payloads near and after landing. However,
                            it's essential to note its limitations, such as the inability to communicate beyond 18,000 meters in altitude. This makes the SPOT Trace a valuable
                            tool for post-landing tracking rather than continuous communication throughout the balloon's flight.</p>

                        <p class="lead mb-3 text-secondary my-auto text-justify">Together, these communication devices create a robust and comprehensive
                            subsystem for weather balloon projects. The LoRa Radio Transceiver at 433MHz provides an efficient and long-range solution
                            for real-time communication, while the SPOT Trace Satellite Tracking device serves as a reliable secondary option for
                            tracking payloads post-landing. The integration of these communication devices ensures a comprehensive and effective system
                            for gathering valuable information from the stratosphere.</p>


